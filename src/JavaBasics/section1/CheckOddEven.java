package JavaBasics.section1;

public class CheckOddEven {
    public static void main(String[] args) {
        
        int number = -2; //enter number here
        
        if (number % 2 == 0) {
            System.out.println("Even number");
        } else {
            System.out.println("Odd number");
        }
        
        System.out.println("Bye!");
        
    }
}
